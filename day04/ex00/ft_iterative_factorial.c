int	ft_iterative_factorial(int nb)
{
	int s;
	int i;

	if (nb < 0 || nb > 12)
	{
		return (0);
	}
	s = 1;
	i = 2;
	while (i <= nb)
	{
		s *= i;
		i++;
	}
	return (s);
}
