#include "sudoku.h"

void	write_grid(int **grid)
{
	int		i;
	int		j;
	char	c;
	int		x;

	x = 0;
	i = 0;
	while (i < 9)
	{
		j = 0;
		while (j < 9)
		{
			x = grid[i][j];
			c = x + '0';
			write(1, &c, 1);
			if (j < 8)
				write(1, " ", 1);
			j++;
		}
		write(1, "\n", 1);
		i++;
	}
}
