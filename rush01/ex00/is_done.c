#include "sudoku.h"

int	is_done(int **grid)
{
	int i;
	int j;
	int ret;

	ret = 1;
	i = 0;
	while (i < 9)
	{
		j = 0;
		while (j < 9)
		{
			if (grid[i][j] == 0)
			{
				ret = 0;
			}
			j++;
		}
		i++;
	}
	return (ret);
}
