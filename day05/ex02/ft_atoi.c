int		ft_atoi(char *str)
{
	int result;
	int i;
	int sign;

	result = 0;
	sign = 1;
	i = 0;
	if (str == '\0')
		return (0);
	while (str[i] == '\n' || str[i] == '\t' || str[i] == ' ' ||
			str[i] == '\r' || str[i] == '\v' || str[i] == '\f')
		i++;
	if (str[i] == '-' || str[i] == '+')
		if (str[i++] == '-')
			sign = -1;
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = (result * 10) + str[i] - '0';
		i++;
	}
	return (sign * result);
}
